package com.yamure.standartprogramlamayapilari

fun main(){
    var s1 = 60
    var s2 = 50

    var y1 = 70
    var  y2 = 80

    println(s1 == s2) //false
    println(s1 != s2) //true
    println(s1 >= s2) //true
    println(s1 < s2) //false

    println(s1 > s2 || y1 > y2) // true or false = true
    println(s1 > s2 && y1 > y2) // true and false = false

}